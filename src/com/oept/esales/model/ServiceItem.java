public class ServiceItem {
	private String serviceItemId;
	private String serviceItemName;
	private String serviceItemDesc;
	private String serviceItemCreated;
	private String serviceItemCreatedBy;
	private String serviceItemUpdate;
	private String serviceItemUpdateBy;
	private String serviceItemComment;
	private String serviceId;
	
	private String createdName;
	private String updateName;
	
	
	public String getCreatedName() {
		return createdName;
	}
	public void setCreatedName(String createdName) {
		this.createdName = createdName;
	}
	public String getUpdateName() {
		return updateName;
	}
	public void setUpdateName(String updateName) {
		this.updateName = updateName;
	}
	public String getServiceItemId() {
		return serviceItemId;
	}
	public void setServiceItemId(String serviceItemId) {
		this.serviceItemId = serviceItemId;
	}
	public String getServiceItemName() {
		return serviceItemName;
	}
	public void setServiceItemName(String serviceItemName) {
		this.serviceItemName = serviceItemName;
	}
	public String getServiceItemDesc() {
		return serviceItemDesc;
	}
	public void setServiceItemDesc(String serviceItemDesc) {
		this.serviceItemDesc = serviceItemDesc;
	}
	public String getServiceItemCreated() {
		return serviceItemCreated;
	}
	public void setServiceItemCreated(String serviceItemCreated) {
		this.serviceItemCreated = serviceItemCreated;
	}
	public String getServiceItemCreatedBy() {
		return serviceItemCreatedBy;
	}
	public void setServiceItemCreatedBy(String serviceItemCreatedBy) {
		this.serviceItemCreatedBy = serviceItemCreatedBy;
	}
	public String getServiceItemUpdate() {
		return serviceItemUpdate;
	}
	public void setServiceItemUpdate(String serviceItemUpdate) {
		this.serviceItemUpdate = serviceItemUpdate;
	}
	public String getServiceItemUpdateBy() {
		return serviceItemUpdateBy;
	}
	public void setServiceItemUpdateBy(String serviceItemUpdateBy) {
		this.serviceItemUpdateBy = serviceItemUpdateBy;
	}
	public String getServiceItemComment() {
		return serviceItemComment;
	}
	public void setServiceItemComment(String serviceItemComment) {
		this.serviceItemComment = serviceItemComment;
	}
	public String getServiceId() {
		return serviceId;
	}
	public void setServiceId(String serviceId) {
		this.serviceId = serviceId;
	}
	
	
}
